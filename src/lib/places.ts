export function randomPlace(exclude?: Set<string>): string {
  const pool = exclude && exclude.size > 0
    ? PLACES.filter((p) => !exclude.has(p))
    : PLACES;
  const list = pool.length > 0 ? pool : PLACES;
  return list[Math.floor(Math.random() * list.length)];
}
